-- Hymn lookup table (global, not ward-scoped).
-- Stores hymns from the standard hymnbook, new hymnbook, and children's songbook.
-- hymn_number: display number (e.g. '1', '341', '1001', 'C1')
-- book: 'STANDARD' | 'NEW' | 'CHILDRENS'
-- sort_key: integer for ordering within search results
-- NOTE: Standard hymn data covers 1-153 completely and selected hymns beyond that.
--       Administrators should verify and complete the list via /support/hymns.

CREATE TABLE IF NOT EXISTS hymn (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  hymn_number TEXT NOT NULL,
  title TEXT NOT NULL,
  book TEXT NOT NULL CHECK (book IN ('STANDARD', 'NEW', 'CHILDRENS')),
  sort_key INTEGER NOT NULL,
  is_active BOOLEAN NOT NULL DEFAULT true,
  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE INDEX IF NOT EXISTS idx_hymn_book_sort ON hymn (book, sort_key);
CREATE INDEX IF NOT EXISTS idx_hymn_active ON hymn (is_active) WHERE is_active = true;

